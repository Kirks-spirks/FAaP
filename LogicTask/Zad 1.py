print("Который час?")

# Количество часов в сутках
DAY = 24
# Часть времени, прошедшая от кусочка конца суток (1/5)
DAY_START = 0.2

# Мы знаем, что прошло 1/5 от конца суток и осталось ещё 5/5 (сам кусочек конца суток).
# Т.о. 6/5 от кусочка конца суток = 24
# Нам надо найти 1/5 кусочек конца суток.
# Для этого мы 24 делим на 6/5, а полученный результат вычитаем из 24  DAY-(DAY/(DAY_START+1))

TimeNow = DAY-(DAY//(DAY_START+1))


print(f"Сейчас, {TimeNow}, часа")